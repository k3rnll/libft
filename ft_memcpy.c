char	*ft_memcpy(char *dst, char *src, int len)
{
	int		i;

	i = 0;
	if (dst == 0 && src == 0)
		return (0);
	while (i < len)
	{
		dst[i] = src[i];
		i++;
	}
	return (dst);
}
